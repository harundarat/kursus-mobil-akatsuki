    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        // TODO add your handling code here:
        try {
            String Query = "UPDATE client SET nik=?, nama=?, alamat=?, hp=? WHERE nik=?";
            Connection con = (Connection) Koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(Query);

            pst.setString(1, tNik.getText());
            pst.setString(2, tNama.getText());
            pst.setString(3, tAlamat.getText());
            pst.setString(4, tHp.getText());
            pst.setString(5, tNik.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data " + tNik.getText() + " berhasil diupdate !!");
            bersih();
            table();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnEditMouseClicked

